    private void labelGuardarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_labelGuardarMousePressed
        // TODO add your handling code here:
        if(chkMusica.isSelected())
        {
            prenderMusica = true;
        }else
        {
            prenderMusica = false;
        }
        String botonJugarAudio = "load.wav";
        InputStream in = null;
        try {
            in = new FileInputStream(botonJugarAudio);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(VentanaConfiguracion.class.getName()).log(Level.SEVERE, null, ex);
        }
        AudioStream nivel1 = null;
        try {
            nivel1 = new AudioStream(in);
        } catch (IOException ex) {
            Logger.getLogger(VentanaConfiguracion.class.getName()).log(Level.SEVERE, null, ex);
        }
        if(prenderMusica)
        {
            AudioPlayer.player.start(nivel1);
        }
        
        try {
            try {
                
                    //System.out.println("oli2");
                    Control.Singleton().setEstadoJuego(Control.EstadosJuego.Menu);
                
                //AudioPlayer.player.stop(nivel1);
            } catch (InterruptedException ex) {
                Logger.getLogger(VentanaConfiguracion.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (IOException ex) {
            Logger.getLogger(VentanaConfiguracion.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        VentanaConfiguracion.Singleton().setVisible(false);
    }//GEN-LAST:event_labelGuardarMousePressed

