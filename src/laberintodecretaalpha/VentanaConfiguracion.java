    private void labelCancelarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_labelCancelarMousePressed
        // TODO add your handling code here:                                                                                                                                                                                                                                                                                                                                                                                                                                  
        if(prenderMusica == false)
        {
            chkMusica.setSelected(false);
        }
        String botonJugarAudio = "load.wav";
        InputStream in = null;
        try {
            in = new FileInputStream(botonJugarAudio);
        } catch (FileNotFoundException ex) {
            Logger.getLogger(VentanaConfiguracion.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
        AudioStream nivel1 = null;
        
        try {
            nivel1 = new AudioStream(in);
        } catch (IOException ex) {
            Logger.getLogger(VentanaConfiguracion.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        if(prenderMusica)
        {
            AudioPlayer.player.start(nivel1);
        }
        
        
        VentanaConfiguracion.Singleton().setVisible(false);
        try {
            try {
                Control.Singleton().setEstadoJuego(Control.EstadosJuego.Menu);
                //AudioPlayer.player.stop(nivel1);
            } catch (InterruptedException ex) {
                Logger.getLogger(VentanaConfiguracion.class.getName()).log(Level.SEVERE, null, ex);
            }
        } catch (IOException ex) {
            Logger.getLogger(VentanaConfiguracion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_labelCancelarMousePressed

